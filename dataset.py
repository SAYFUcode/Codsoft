# ---------------------------------------------------------------------------
# Dataset builder
# ---------------------------------------------------------------------------

def build_dataset(samples_per_genre: int = 50) -> pd.DataFrame:
    """Return a shuffled DataFrame with columns ``plot`` and ``genre``.

    Parameters
    ----------
    samples_per_genre:
        Maximum number of plots to include per genre.  Defaults to 50
        (i.e. all available plots, since the corpus has 40 per genre).

    Returns
    -------
    pd.DataFrame
        Columns: ``plot`` (str), ``genre`` (str).
    """
    rows = [
        {"plot": plot, "genre": genre}
        for genre, plots in GENRE_PLOTS.items()
        for plot in plots[:samples_per_genre]
    ]
    df = pd.DataFrame(rows)
    return df.sample(frac=1, random_state=42).reset_index(drop=True)
